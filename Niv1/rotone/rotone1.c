#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	str;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			str = av[1][i];
			if (av[1][i] >= 'A' && av[1][i] <= 'Y')
				str += 1;
			if (av[1][i] >= 'a' && av[1][i] <= 'y')
				str += 1;
			if (av[1][i] == 'Z' || av[1][i] == 'z')
				str -= 25;
			write(1, &str, 1);
			++i;
		}
	}
	write(1, "\n", 1);
	return (0);
}