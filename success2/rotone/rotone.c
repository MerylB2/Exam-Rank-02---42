#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	char 	rot;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			rot = av[1][i];
			if (av[1][i] >= 'a' && av[1][i] <= 'y')
				rot += 1;
			if (av[1][i] >= 'A' && av[1][i] <= 'Y')
				rot += 1;
			if (av[1][i] == 'z' || av[1][i] == 'Z')
				rot -= 25;
			write(1, &rot, 1);
			++i;
		}
	}
	write(1, "\n", 1);
	return (0);
}
